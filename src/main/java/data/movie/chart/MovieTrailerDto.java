package data.movie.chart;

public class MovieTrailerDto {

	private String trailerId;
	private String movieId;
	private String trailerLink;
	private String trailerImg;
	private String trailerTitle;

	public String getTrailerId() {
		return trailerId;
	}

	public void setTrailerId(String trailerId) {
		this.trailerId = trailerId;
	}

	public String getMovieId() {
		return movieId;
	}

	public void setMovieId(String movieId) {
		this.movieId = movieId;
	}

	public String getTrailerLink() {
		return trailerLink;
	}

	public void setTrailerLink(String trailerLink) {
		this.trailerLink = trailerLink;
	}

	public String getTrailerImg() {
		return trailerImg;
	}

	public void setTrailerImg(String trailerImg) {
		this.trailerImg = trailerImg;
	}

	public String getTrailerTitle() {
		return trailerTitle;
	}

	public void setTrailerTitle(String trailerTitle) {
		this.trailerTitle = trailerTitle;
	}

}
